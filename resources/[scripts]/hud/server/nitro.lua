-----------------------------------------------------------------------------------------------------------------------------------------
-- VARIABLES
-----------------------------------------------------------------------------------------------------------------------------------------
GlobalState["Nitro"] = {}
-----------------------------------------------------------------------------------------------------------------------------------------
-- UPDATENITRO
-----------------------------------------------------------------------------------------------------------------------------------------
function Hensa.UpdateNitro(Plate, NitroFuel)
	if GlobalState["Nitro"][Plate] then
		local Nitro = GlobalState["Nitro"]
		Nitro[Plate] = NitroFuel
		GlobalState:set("Nitro", Nitro, true)
	end
end