RegisterServerEvent("inventory:StoreObjects")
AddEventHandler("inventory:StoreObjects",function(Number)
	local source = source
	local Passport = vRP.Passport(source)
	if Passport and not Active[Passport] and Objects[Number] then
		Active[Passport] = true

		if Objects[Number] and Objects[Number]["Timer"] and Objects[Number]["Timer"] >= os.time() then
			TriggerClientEvent("Notify",source,"Atenção","Aguarde "..CompleteTimers(Objects[Number]["Timer"] - os.time())..".","amarelo",5000)
			Active[Passport] = nil

			return false
		end

		if Objects[Number] and Objects[Number]["Item"] and Objects[Number]["Item"] ~= "spikestrips" then
			if not vRP.MaxItens(Passport,Objects[Number]["Item"],1) and vRP.CheckWeight(Passport,Objects[Number]["Item"],1) then
				vRP.GiveItem(Passport,Objects[Number]["Item"],1,true)
			else
				TriggerClientEvent("Notify",source,"Mochila Sobrecarregada","Sua recompensa caiu no chão.","roxo",5000)
				exports["inventory"]:Drops(Passport,source,Objects[Number]["Item"],1)
			end
		end

		if SaveObjects[Number] then
			SaveObjects[Number] = nil
		end

		TriggerClientEvent("objects:Remover",-1,Number)
		Active[Passport] = nil
		Objects[Number] = nil
	end
end)